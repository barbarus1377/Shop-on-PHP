    public static function sorting($sort) {
        
        switch ($sort) {
            case '0';
                $sort = 'type = "0"';
                break;
            case '1';
                $sort = 'type = "1"';
                break;
            case '2';
                $sort = 'type = "2"';
                break;
            case '3';
                $sort = 'type = "3"';
                break;
            default:
                $sort = '*';
                break;
        }
        return $sort;
    }
    /**
     * Возвращает массив последних товаров
     * @param type $count [optional] <p>Количество</p>
     * @param type $page [optional] <p>Номер текущей страницы</p>
     * @return array <p>Массив с товарами</p>
     */
    public static function getLatestProducts($count = self::SHOW_BY_DEFAULT, $page = 1) {
        // Смещение (для запроса)
        $offset = ($page - 1) * $count;
        // Соединение с БД
        $db = Db::getConnection();
        // Текст запроса к БД
        $sql = 'SELECT id, name, price, is_sale FROM product '
                . 'ORDER BY id DESC '
                . 'LIMIT :count OFFSET :offset';

        // Используется подготовленный запрос
        $result = $db->prepare($sql);
        $result->bindParam(':count', $count, PDO::PARAM_INT);
        $result->bindParam(':offset', $offset, PDO::PARAM_INT);

        // Указываем, что хотим получить данные в виде массива
        $result->setFetchMode(PDO::FETCH_ASSOC);

        // Выполнение коменды
        $result->execute();

        // Получение и возврат результатов
        $i = 0;
        $productsList = array();
        while ($row = $result->fetch()) {
            $productsList[$i]['id'] = $row['id'];
            $productsList[$i]['name'] = $row['name'];
            $productsList[$i]['price'] = $row['price'];
            $productsList[$i]['is_sale'] = $row['is_sale'];
            $i++;
        }
        return $productsList;
    }

