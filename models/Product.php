    /**
     * Возвращает путь к изображению
     * @param integer $id
     * @return string <p>Путь к изображению</p>
     */
    public static function getImage($id) {
        // Название изображения-пустышки
        $noImage = 'no-image.jpg';

        // Путь к папке с товарами
        $path = '/upload/images/products/';

        // Путь к изображению товара
        $pathToProductImage = $path . $id . '.png';

        if (file_exists($_SERVER['DOCUMENT_ROOT'] . $pathToProductImage)) {
            // Если изображение для товара существует
            // Возвращаем путь изображения товара
            return $pathToProductImage;
        }

        // Возвращаем путь изображения-пустышки
        return $path . $noImage;
    }

    /**
     * Возвращает товары по результатам поиска
     * @param integer $words
     * @return array <p>Массив с товарами</p>
     */
    public static function search($words) {
        // Соединение с БД
        $db = Db::getConnection();

        $words = htmlspecialchars($words);
        if ($words === "") {
            return false;
        }
        $query_search = "";

        $arraywords = explode(" ", $words);
        foreach ($arraywords as $key => $value) {
            if (isset($arraywords[$key - 1])) {
                $query_search .= '`name` LIKE "%' . $value . '%';
            }
        }
        $sql = "SELECT * FROM product WHERE $query_search";

        // Используется подготовленный запрос
        $result = $db->prepare($sql);

        // Указываем, что хотим получить данные в виде массива
        $result->setFetchMode(PDO::FETCH_ASSOC);

        // Выполнение коменды
        $result->execute();

        // Получение и возврат результатов
        $i = 0;
        $searchProduct = array();
        while ($row = $result->fetch()) {
            $searchProduct[$i]['id'] = $row['id'];
            $searchProduct[$i]['name'] = $row['name'];
            $searchProduct[$i]['price'] = $row['price'];
            $searchProduct[$i]['is_sale'] = $row['is_sale'];
            $i++;
        }
        return $searchProduct;
    }

